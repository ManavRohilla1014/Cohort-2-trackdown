function countVowels(str) {
    // Your code here
    let result = str.toLowerCase();
    let count = 0;
    for(let i=0; i<result.length;i++){
      if(result[i] == 'a' || result[i] == 'e' || result[i] == 'i' || result[i] == 'o' || result[i] == 'u'){
        count++;
      }
    }
    return count;
}

module.exports = countVowels;